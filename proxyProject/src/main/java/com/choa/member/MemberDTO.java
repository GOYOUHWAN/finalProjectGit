package com.choa.member;

public class MemberDTO {
	private String id;
	private String pw;
	private String name;
	private String tel;
	private String birth;
	private String email;
	private String address;
	private int type;
	private int grade;
	private int buyer_trust;
	private int seller_trust;
	private String account;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public int getBuyer_trust() {
		return buyer_trust;
	}
	public void setBuyer_trust(int buyer_trust) {
		this.buyer_trust = buyer_trust;
	}
	public int getSeller_trust() {
		return seller_trust;
	}
	public void setSeller_trust(int seller_trust) {
		this.seller_trust = seller_trust;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	
	
	
	

}
